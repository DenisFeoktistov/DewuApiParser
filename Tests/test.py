def merge_dicts(data):
    merged_data = defaultdict(lambda: {"count": 0, "examples": []})

    for key, value in data.items():
        merged_data[key]["count"] += value["count"]  # Суммируем count
        merged_data[key]["examples"].extend(value["examples"])  # Объединяем списки examples

    return dict(merged_data)


def sort_by_count(data):
    # Сортируем словарь по значению 'count' и возвращаем отсортированный словарь
    return dict(sorted(data.items(), key=lambda item: item[1]['count'], reverse=True))


